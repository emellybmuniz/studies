public class primitiveTypes {
    byte number = 10; // 8 bits, range: -128 to 127
    short smallNumber = 1000; // 16 bits, range: -32,768 to 32,767
    int integerNumber = 100000; // 32 bits, range: -2,147,483,648 to 2,147,483,647
    long longNumber = 10000000000L; // 64 bits, range: -9,223,372,036,854,775,808 to 9,223,372,036,854,775,807
    float floatNumber = 10.5f; // 32 bits, range: approximately -3.40282347E+38 to 3.40282347E+38
    double doubleNumber = 20.99; // 64 bits, range: approximately -1.79769313486231570E+308 to 1.79769313486231570E+308
    char character = 'A'; // 16 bits, represents a single Unicode character
    boolean isTrue = true; // 1 bit, can be either true or false
    boolean isFalse = false; 
    char nullCharacter = '\u0000'; // Represents a null character in Unicode

}
